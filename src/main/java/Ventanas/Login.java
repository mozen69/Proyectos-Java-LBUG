    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        // TODO add your handling code here:
        String nombre = txtUsuario.getText();
        String pass = String.valueOf(jPass.getPassword());
        String sql = "SELECT * FROM Usuarios WHERE Nombre ='" + nombre + "' and Clave ='" + pass + "'";
        Conector con = new Conector();
        con.connect();
        if (con.validaUsuario(sql)) {
            Escritorio escritorio = new Escritorio();
            this.dispose();
            escritorio.setVisible(true);
            con.close();

        } else {
            JOptionPane.showMessageDialog(null, "¬° ACCESO DENEGADO !" + "\n\nVerifique sus credenciales");
            //con.limpiarFormulario(this.getContentPane().getComponents());
            Generales generales = new Generales();
            generales.limpiarFormulario(this.getContentPane().getComponents());
            con.close();
        }
    }//GEN-LAST:event_btnValidarActionPerformed

