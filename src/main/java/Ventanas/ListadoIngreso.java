    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // String sql = "INSERT INTO Entradas (Fecha, N_Documento, Id_Material, Cantidad, Observacion,Id_Usuario) VALUES('" + fecha + "','" + numDocumento + "','" + IdMaterial + "','" + cantidad + "','" + observaciones + "','" + IdUsuario + "')";
        Conector con = new Conector();
        con.connect();
        ResultSet rs = null;
        int Id_Material;
        String plantilla = "dd-MM-yyyy";
        Date fecha = Ingresos.Fecha.getDate();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat(plantilla);
        String Fecha = simpleDateFormat.format(fecha);
        int N_Documento = Integer.parseInt(Ingresos.txtDocumento.getText());
        int Id_Usuario = Escritorio.idData[1];
        try {
            for (int i = 0; i < TablaListado.getRowCount(); i++) {
                String codigo = (String) TablaListado.getValueAt(i, 1);
                rs = con.buscarId(codigo);
                Id_Material = rs.getInt("Id");
                PreparedStatement st = con.connect.prepareStatement("INSERT INTO Entradas (Fecha, N_Documento, Id_Material, Cantidad, Observacion, Id_Usuario) VALUES (?,?,?,?,?,?)");
                st.setString(1, Fecha);//guardamos fecha
                st.setInt(2, N_Documento);//gyardamos documento
                st.setInt(3, Id_Material);//guardamos id_material
                st.setInt(4, Integer.parseInt(TablaListado.getValueAt(i, 3).toString()));
                st.setString(5, TablaListado.getValueAt(i, 5).toString());// guardamos observaciones
                st.setInt(6, Id_Usuario);//guardamos id_usuario
                st.executeUpdate();
            }//cierre de for
            Ingresos.Fecha.setCalendar(null);
            Ingresos.txtDocumento.setText("");
            Ingresos.modeloListado.setRowCount(0);
            Ingresos.Item = 1;
            JOptionPane.showMessageDialog(null, "Se agregaron lo datos correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (SQLException ex) {
            Logger.getLogger(ListadoIngreso.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

