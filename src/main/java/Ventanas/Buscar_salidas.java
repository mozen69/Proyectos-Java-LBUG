    private void sldtxtClaveKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sldtxtClaveKeyTyped
         String spClave = sldtxtClave.getText();
        String sql="SELECT Codigo, Descripcion FROM Maestro WHERE Descripcion LIKE '%"+spClave+"%'";
        Conector con = new Conector();
        con.connect();
        ResultSet rs= con.buscar(sql);
        try {
            ResultSetMetaData rsMD = rs.getMetaData();
            int numCol = rsMD.getColumnCount();
            DefaultTableModel sldtablabuscar = new DefaultTableModel() {
                 @Override
                  public boolean isCellEditable(int row, int column) {
                  return false;
                 }
                };
            Buscar_salidas.sldTablabuscar.setModel(sldtablabuscar);
            for (int x=1; x<=2; x++){
                sldtablabuscar.addColumn(rsMD.getColumnLabel(x));
            }
            
            while (rs.next()){
                Object[] fila =new Object [numCol];
                for (int y=0; y<=1; y++){
                fila[y]=rs.getObject(y+1);
                }
                sldTablabuscar.getColumnModel().getColumn(0).setMinWidth(100);
                sldTablabuscar.getColumnModel().getColumn(0).setMaxWidth(100);
                sldtablabuscar.addRow(fila);    
            }
        } catch (SQLException ex) {
            Logger.getLogger(Buscar_salidas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }                                         

