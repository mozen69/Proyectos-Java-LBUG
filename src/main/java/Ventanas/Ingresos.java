    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();
        if ((car < '0' || car > '9') && (car != '-') && (car != 'N' && car != 'n' && car != 'O' && car != 'o' && car != 'C' && car != 'c'))
            evt.consume();
    }//GEN-LAST:event_txtCodigoKeyTyped

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (!camposVacios()){
        String codigo = txtCodigo.getText();
        String detalle = txtDetalle.getText();
        int cantidad = Integer.parseInt(txtCantidad.getText());
        String unidad = txtUnidad.getText();
        String observaciones = txtObservaciones.getText();

        modeloListado.addRow(new Object[]{Item, codigo, detalle, cantidad, unidad, observaciones});
        ListadoIngreso.TablaListado.setModel(modeloListado);
        Item++;
        txtCodigo.setText("");
        txtDetalle.setText("");
        txtCantidad.setText("");
        txtUnidad.setText("");
        txtDetalle.setText("");
        txtObservaciones.setText("");
        txtCodigo.requestFocus();
        }else{
             JOptionPane.showMessageDialog(null, "Existen campos vacios!!", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
             txtDocumento.requestFocus();
        
        
        }
        

    }//GEN-LAST:event_btnAgregarActionPerformed

