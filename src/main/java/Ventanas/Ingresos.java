    private void txtDetalleFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDetalleFocusGained
        // TODO add your handling code here:
        Conector con = new Conector();
        con.connect();
        ResultSet rs = null;
        int Id;
        int caso = 0;
        String clave = this.txtCodigo.getText();
        String sql = "SELECT Id, Descripcion, Unidad FROM Maestro WHERE Codigo ='" + clave + "'";
        rs = con.buscar(sql);
        try {
            if (!rs.next()) {
                caso = 1;
                this.txtDetalle.setText("CODIGO NO ARROJA RESULTADOS!!!");
                this.txtCodigo.requestFocus();
                con.close();
            } else {
                caso = 2;
                Id = rs.getInt("Id");
                Escritorio.idData[0] = Id;
                this.txtDetalle.setText(rs.getString("Descripcion"));
                this.txtUnidad.setText(rs.getString("Unidad"));
                con.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ingresos.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_txtDetalleFocusGained

